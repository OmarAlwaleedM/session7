s = "ABCDEFGHIJKLMNOP"

# index is also a very basic slice
print(s[1]) # B
print(s[1:3]) # from postion 1 to position 3 excluded
print(s[5:9])
print(s[:7]) # from start to postion 7 (excluding 7)
print(s[4:])
print(s[:]) # all of it
print(s[::2]) # all of it in steps of 2
print(s[:10:3]) # ADGJ
print(s[::-1]) # all of it but reversed



